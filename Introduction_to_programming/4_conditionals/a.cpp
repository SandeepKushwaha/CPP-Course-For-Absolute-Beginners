#include<iostream>
using namespace std;

/**
 * if statement:
 *      The if statement is used to check a condition and execute 
 *      a block of code only if the condition is true. If the condition is false, 
 *      the block of code inside the if statement will be skipped.
 * 
 */

int main() {

    int x = 10;
    if (x == 10) {
        cout << "x is equal to 10" << endl; 
    }

    return 0;
}