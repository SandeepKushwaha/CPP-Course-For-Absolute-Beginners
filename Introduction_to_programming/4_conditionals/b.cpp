#include <iostream>
using namespace std;

/**
 * 
 * If-else statement:
 *      The if-else statement is used to execute a block of code if the condition is true, 
 *      and another block of code if the condition is false.
 * 
 */

int main() {
    int x = 5;
    if (x == 10) {
        cout << "x is equal to 10" << endl;
    }
    else {
        cout << "x is not equal to 10" << endl;
    }
    return 0;
}
