#include <iostream>
using namespace std;

int main() {

    int x;  // declares an integer variable named x
    x = 10; // assigns the value 10 to x

    float y;  // declares a floating-point variable named y
    y = 3.14; // assigns the value 3.14 to y

    char z;  // declares a character variable named z
    z = 'A'; // assigns the value 'A' to z

    bool flag;   // declares a boolean variable named flag
    flag = true; // assigns the value true to flag

    string name;   // declares a string variable named name
    name = "John"; // assigns the value "John" to name

    return 0;
}